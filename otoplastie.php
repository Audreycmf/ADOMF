												<h1>L'otoplastie</h1>
											</header>
											<p>
                        L’intervention chirurgicale destinée à corriger les oreilles décollées est appelée <strong>otoplastie</strong>. Elle vise à remodeler le
                        cartilage de façon à obtenir des oreilles "recollées", symétriques, de taille et d'aspect naturels, permettant ainsi de mettre
                        fin aux moqueries et autres remarques désobligeantes susceptibles d'être à l'origine de difficultés psychologiques ou de
                        conflits scolaires.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Consultation</h2>
                      <p>
                        Les oreilles décollées sont le résultat d’anomalies de forme (défaut de plicature) et/ou de taille (hypertrophie de conque) au
                        niveau du cartilage de l’oreille. La consultation permet de déterminer la part de chaque anomalie et de vous expliquer les
                        modalités de la correction chirurgicale, de choisir le mode d’anesthésie (locale ou générale) le plus adapté au patient et en
                        particulier à son âge.<br />
                        Chez l'enfant, il existe des périodes plus propices pour réaliser l’intervention : après la fin de la période de croissance du
                        pavillon de l'oreille (vers 7 ou 8 ans), en dehors de la puberté. Par ailleurs, il est important d’évaluer la motivation de
                        l’enfant.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">L’intervention</h2>
                      <p>
                        Que l’intervention soit sous anesthésie locale ou générale, le technique est identique : remodelage du  cartilage et retrait
                        d’une languette de peau en arrière de l’oreille. Les cicatrices sont dissimulées en arrière de l’oreille.<br />
                        La tête et les cheveux seront soigneusement lavés la veille de l'opération. Il n’est pas nécessaire de les couper.<br />
                        Habituellement l'intervention se pratique en "ambulatoire" c'est-à-dire en hospitalisation de jour.<br />
                        En fin d’intervention, un pansement compressif est appliqué.<br />
                        La durée de l’intervention est en moyenne d’une heure.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Suites opératoires</h2>
                      <p>
                        Les suites opératoires sont les suivantes :
                        <ul class="basic-list">
                          <li>un œdème (gonflement) et des petits hématomes (bleu) modérés sont normaux</li>
                          <li>des antalgiques et des anti-inflammatoires sont prescrits pour la douleur</li>
                          <li>consultation à 48h pour retirer le pansement compressif. Il est remplacé par la prescription de port d'un bandeau
                            protecteur jour et nuit pendant 15 jours puis uniquement la nuit pendant 15 jours de plus pour éviter le retournement accidentel de l'oreille (risque de lâchage des sutures).
                            La pratique sportive est contre-indiquée pour la même raison</li>
                          <li>les fils sont retirés vers le 10ème jour</li>
                          <li>prévoir quelques jours de dispense scolaire ou d’arrêt de travail</li>
                          <li>l'exposition au grand froid est déconseillée pendant deux mois (risque de gelures du fait de la diminution transitoire de
                            la sensibilité des oreilles)</li>
                          <li>un délai de un à deux mois est nécessaire pour apprécier le résultat final. C'est le temps nécessaire pour que les tissus
                            se soient assouplis et que la totalité de l'oedème se soit résorbé</li>
                        </ul>
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Complications</h2>
                      <p>
                        Bien que tous les efforts soient mis en oeuvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser une <strong>otoplastie</strong>, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>imperfections de résultats : légère asymétrie, petites irrégularités, récidive. Ces petits défauts, quand ils existent,
                            sont habituellement discrets et n'attirent pas le regard. Pour autant,  ils sont toujours accessibles à une petite
                            "retouche" qui se fera la plupart du temps sous simple anesthésie locale</li>
                          <li>trouble de la sensibilité de l’oreille : s’estompe avec le temps mais peut gêner le port des lunettes</li>
                          <li>hématomes : ils surviennent dans les 2 premiers jours. Sources de douleurs importantes, ils doivent être drainés pour
                            éviter les déformations secondaires</li>
                          <li>cicatrices hypertrophiques, chéloïdiennes : rares sur les peaux blanches, plus fréquentes sur les peaux pigmentées. Malgré
                            toute l’attention portée aux sutures, les cicatrices en arrière du pavillon de l’oreille peuvent être le siège d’une
                            hypertrophie gênante voire d’une évolution "chéloïdienne" (pérennisation de l’hypertrophie cicatricielle) dont le
                            traitement reste difficile</li>
                          <li>chondrite (infection du cartilage) : très exceptionnelle, elle peut conduire à la destruction de l’oreille</li>
                          <li>nécrose cutanée : exceptionnelle, elle survient parfois du fait de trouble de la circulation sur la peau très fine de la
                            face antérieure du pavillon, en regard d’un relief cartilagineux. La cicatrisation intervient grâce à des pansements locaux
                            en laissant une petite plage cicatricielle</li>
												</ul>
											</p>
