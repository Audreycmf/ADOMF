										<section>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Techniques</h3>
											<p>
                        Pour commencer il faut savoir que toute intervention chirurgicale traverse la peau. Quelle que soit la technique utilisée pour
                        recoudre celle-ci, une cicatrice en résultera, elle s’atténuera pour devenir discrète mais ne sera jamais invisible.<br />
                        Le principe de base est l'ablation "en fuseau" suivie d'une suture directe par rapprochement des berges. Dans le cas des lésions
                        malignes, une marge de sécurité dont l'ampleur dépend du type de lésion est rajoutée (<a href="#fuseau">cf.schéma</a>).<br />
                        Le  fuseau permet d’éviter la formation de replis aux extrémités de la cicatrice lors de la fermeture en diminuant la tension
                        sur les berges, mais aboutit à une cicatrice dont la taille est supérieure au diamètre de la lésion, MAIS à long terme le
                        résultat esthétique sera meilleur.<br />
                        De plus, la discrétion de la cicatrice sera favorisée par l'orientation de l'incision dans l'axe des plis naturels de la peau
                        (<a href="#langer">cf. schéma</a>) et par une technique de suture irréprochable.<br />
                        Dans certains  cas, la fermeture par suture directe est irréalisable à cause de la taille ou de l’endroit de la lésion. La
                        couverture de la zone est assurée alors soit par une greffe de peau prélevée sur une autre région discrète, soit par un lambeau
                        (déplacement de peau avoisinante pour recouvrir la perte de substance cutanée : <a href="#lambeau">cf. schéma</a>). Enfin dans de très rares cas, nous
                        utilisons l’expansion cutanée réalisée grâce à des ballonnets gonflables placés sous la peau saine avoisinante (le gonflage
                        progressif entraîne un développement cutané sur plusieurs semaines; comme lors de la grossesse).<br />
                        Dans tous les cas, votre chirurgien vous expliquera la solution adaptée à votre cas personnel.
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Consultation</h3>
                      <p>
                        Le chirurgien mènera un interrogatoire suivi d'un examen de la lésion cutanée afin de vous expliquer la ou les possibilité(s)
                        chirurgicale(s) et le type d’anesthésie retenue.<br />
                        L’arrêt du tabac est recommandé un mois avant et un mois après l’intervention (risque d’un retard de cicatrisation).<br />
                        Aucun médicament contenant de l'Aspirine ne devra être pris dans les 10 jours précédant l'intervention.<br />
                        Aucun maquillage ni aucun bijou ou piercing ne devra être porté durant l'opération.
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">L’intervention</h3>
                      <p>
                        Les modalités de l’intervention sont directement liées au type d’anesthésie choisie et vous auront été détaillées pendant la
                        consultation.
                        <ul class="basic-list">
                          <li>Anesthésie locale : un produit anesthésiant est injecté autour de la zone à opérer afin d'assurer l'insensibilité. C'est
                            le cas le plus fréquent. L’intervention ne nécessite pas une hospitalisation et peut, à l'instar des soins dentaires, être
                            réalisée à l'hôpital "en externe", c'est-à-dire avec une entrée juste avant l'opération et une sortie juste après celle-ci.</li>
                          <li>Anesthésie "vigile" (Anesthésie locale + tranquillisants) : vous pouvez rester éveillé mais où vous serez relaxé. Elle
                            peut être préférée pour des raisons de confort personnel, de votre état général. Dans ce cas, il y a nécessité d’une
                            hospitalisation "en ambulatoire" (=hospitalisation de jour) avec une sortie le jour même après quelques heures de surveillance.</li>
                          <li>Anesthésie générale classique : vous dormez complètement. Ce type d’anesthésie est peu fréquent en chirurgie cutanée.
                            L'hospitalisation se fera "en ambulatoire" et parfois de façon traditionnelle, avec une nuit à passer sur place (cela reste
                            exceptionnel pour la chirurgie cutanée).</li>
                        </ul>
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Suites opératoires</h3>
                      <p>
                        Dans les 48 premières heures, il peut apparaître un œdème (gonflement) et un petit hématome (bleu) qui ne sont que transitoires,
                        un petit saignement peut tacher légèrement le pansement. Des démangeaisons sont par ailleurs assez fréquentes durant la phase
                        de cicatrisation. Les douleurs véritables invalidantes sont rares, une sensation de tension et d’inconfort est classique,
                        cédant facilement avec une prescription de Paracétamol.<br />
                        Toutes ces constatations ne sont pas inquiétantes et doivent être considérées comme des suites "habituelles".<br />
                        Suivant la zone opérée (épaule, dos..), il faut éviter les mouvements qui « tirent » sur la cicatrice.<br />
                        Les fils ne sont pas résorbables et seront  retirés entre le 5ème et le 15ème jour suivant la zone opérée. Le chirurgien vous
                        expliquera les modalités des soins à apporter à votre cicatrice pour les semaines suivantes (massages, protection solaire…).<br />
                        Concernant l'évolution cicatricielle habituelle, il faut noter qu'initialement la cicatrice est souvent rouge ou rosée, puis
                        virer au brun et devenir fibreuse, indurée, avant de s'éclaircir et de s'assouplir (après le troisième mois).<br />
                        Pendant 6 à 12mois, il faut limiter l’exposition solaire et d'utiliser une protection type "écran total".<br />
                        L'aspect de la cicatrice prend son aspect définitif après le 3-4 mois mais elle se stabilise réellement au bout de plusieurs
                        mois (12-24 mois).<br />
                        Dans le cas d'une lésion cutanée maligne, l'examen anatomopathologique au microscope peut conclure à la nécessité d’une reprise
                        chirurgicale (les limites sont parfois floues et difficiles à déterminer précisément à l'œil nu, il existe un risque d’exérèse
                        incomplète ou avec une marge de sécurité insuffisante).<br />
                        Même si l'ablation d'une lésion a été jugée complète, y compris par l'examen anatomopathologique, on peut parfois assister,
                        même des années après, à une "récidive" locale de la lésion. En effet certaines lésions sont minimes, non décelables lors de la
                        première intervention. Elles peuvent donc être initialement épargnées et ensuite se développer pour leur propre compte.<br />
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Complications</h3>
                      <p>
                        Bien que tous les efforts soient mis en oeuvre pour rendre les cicatrices les plus discrètes possible, il arrive que la
                        cicatrisation ne soit pas satisfaisante. La qualité de la cicatrisation est variable selon l'âge, les parties du corps, les
                        facteurs environnementaux (tabac), les autres pathologies du patient et d'un individu à l'autre.<br />
                        La chirurgie des lésions cutanées comporte, comme tout acte médical si minime soit-il, un certain nombre d'incertitudes et de
                        risques.<br />
                        En choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans
                        toutefois les supprimer complètement. Les complications significatives restent exceptionnelles.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>Petits saignements : ils peuvent survenir même quelques jours après l'intervention. Ils sont habituellement faciles à
                            contrôler</li>
                          <li>Hématomes : la plupart du temps sans gravité, ils peuvent justifier d'être évacués s'ils sont trop importants</li>
                          <li>Infection : elle peut être liée à une forme d'intolérance aux fils de suture ou être favorisée par la présence d'un petit
                            hématome. Le recours à des antibiotiques ou de simples soins locaux permettent de résoudre le problème, parfois avec des
                            conséquences néfastes sur la qualité de la cicatrice</li>
                          <li>Nécrose cutanée : il s'agit d'une mortification de la peau par défaut d'apport sanguin. Elle est exceptionnelle et ne
                            se rencontre que dans les cas de tension extrême sur les berges de la cicatrice ou lors de la réalisation d'un lambeau à
                            la vascularisation précaire. Elle peut parfois venir compliquer un hématome ou une infection. Elle est nettement favorisée
                            par le tabagisme</li>
                          <li>Défaut de prise de greffe : la réussite d'une greffe n'est jamais certaine à 100%</li>
                          <li>Anomalies de cicatrisation : au-delà des cicatrices inesthétiques déjà évoquées, on pense surtout aux redoutables mais
                            rarissimes cicatrices "chéloïdes" vraies, dont le traitement est très délicat et souvent décevant</li>
                          <li>Troubles sensitifs : on peut observer des troubles localisés de la sensibilité (anesthésie, fourmillements…). La plupart
                            du temps ces troubles sont transitoires et disparaissent spontanément en quelques semaines</li>
                        </ul>

                        En conclusion : &laquo; Il faut bien comprendre que la cicatrisation reste un phénomène aléatoire dont la qualité ne peut en
                        aucun cas être garantie. La parfaite maîtrise technique d'un chirurgien plasticien qualifié et spécifiquement formé à ce type
                        d'intervention permet de mettre toutes les chances de son côté mais ne supprime pas cet aspect aléatoire.&raquo;
											</p>
