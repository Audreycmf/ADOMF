                    <section>
                      <p>
                        La chirurgie dermatologique ou cutanée est la prise en charge des lésions bénignes cutanées et sous-cutanées (grains de beauté,
                        kystes épidermiques, lipomes, etc…) et des lésions malignes de la peau (carcinome baso-cellulaire, spino-cellulaire, mélanome…).<br />
                        Les lésions cancéreuses siègent fréquemment sur le visage, car elles sont souvent liées à l’exposition solaire. Cette chirurgie
                        peut s’effectuer sous courte anesthésie locale la plupart du temps.<br />
                        Les moyens de réparation d’une lésion cutanée cancéreuse sont la suture directe (lorsque la lésion est petite), la greffe de
                        peau (prélèvement de peau sur une autre zone discrète du corps), ou les lambeaux (mobilisation de la peau adjacente à la lésion
                        pour fermer la perte de substance tissulaire).
                      </p>
