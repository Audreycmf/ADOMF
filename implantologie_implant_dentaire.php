											<header>
												<h1>L'implant dentaire</h1>
											</header>
											<p>
                        Un <strong>implant dentaire</strong> est une "racine" artificielle permettant le remplacement d’une dent manquante. Il s’agit d’une
                        vis en titane qui est ancrée dans l’os (mâchoire supérieure ou inférieure) et qui permet de recevoir la prothèse (fausse
                        couronne dentaire) par l’intermédiaire d’un pilier.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Consultation</h2>
                      <p>
                        La consultation permet de faire une évaluation de la possibilité de mise en place d'implants. Un cone beam vous sera demandé afin
                        de déterminer si vous devez prélablement passer par une étape de <a href="preimplantaire.php">chirurgie pré-implantaire</a>.<br />
                        Ce sera également l'occasion de détecter les contre-indications. Seuls le chirurgien et le praticien sont aptes à poser
                        l’indication ou non. Il y a 2 types de contre-indications :
                        <ul class="basic-list">
                          <li>les contre-indications absolues :
                            <ol class="level2_list">
                              <li>certains antécédents du patient : pathologies cardiaques (valvulaires, insuffisance cardiaque, infarctus myocardique
                                récent...), SIDA déclaré, cancers évolutifs et maladies auto-immunes ou leurs traitements (radiothérapie,
                                immmuno-supresseurs)</li>
                              <li>les traitements par biphosphonates pour des maladies malignes. En revanche, dans
                                le traitement de l’ostéoporose, ce n’est pas considéré comme une contre-indication absolue devant des risques rares
                                mais aux conséquences grandissimes dont le patient doit être clairement être informé en détail</li>
                            </ol>
                          </li>
                          <li>les contre-indications relatives :
                            <ol class="level2_list">
                              <li>le diabète</li>
                              <li>le consommation de tabac</li>
                              <li>la séropositivité</li>
                              <li>la grossesse</li>
                              <li>l’âge (la fin de la croissance est indispensable)</li>
                              <li>contre-indications locales relatives : la principale difficulté est le manque d’os. La pose d’implants dentaires
                                nécessite la présence d’os en quantité et qualité satisfaisantes. Une  greffe osseuse en amont peut, dans certaines
                                situations, remédier à ce déficit osseux. Dans d’autres circonstances, il sera impossible de reconstruire l’os manquant
                                et de poser des implants</li>
                              <li>le présence de foyers infectieux dentaires ou sinusiens ou une hygiène bucco-dentaire insuffisante</li>
                              <li>le bruxisme, les anomalies structurelles des mâchoires qui peuvent entraîner des contraintes mécaniques trop fortes
                                sur les implants</li>
                            </ol>
                          </li>
                        </ul>
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">L’intervention</h2>
                      <p>
                        L’intervention se déroule généralement sous anesthésie locale. Le chirurgien incise la gencive et passe différents forêts dans
                        l’os de la mâchoire afin de placer l’<strong>implant dentaire</strong>. La cicatrisation de celui-ci (appelée ostéointégration) varie entre 3 à 6 mois
                        avant la pose de la prothèse.<br />
                        L’implant et la prothèse peuvent être posés le même jour mais ces indications sont très précises et nécessitent une étroite
                        collaboration entre le chirurgien et le praticien.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Suites opératoires</h2>
                      <p>
                        Les suites post-opératoires sont en général :
                        <ul class="basic-list">
                          <li>des douleurs légères calmées par la prise d’anti-douleur de palier 1 (type paracétamol)</li>
                          <li>un léger œdème les premiers jours, surtout en cas de pose de plusieurs implants</li>
                          <li>un léger saignement est possible</li>
                        </ul>
                        Les consignes post-opératoires sont :
                        <ul class="basic-list">
                          <li>une alimentation molle et froide ou tiède les premiers jours</li>
                          <li>l'interdiction de porter une prothèse amovible qui prendrait appui sur l’<strong>implant dentaire</strong></li>
                          <li>la consommation tabagique est contre-indiquée car elle retarde la cicatrisation et augmente le risque d’infection</li>
                          <li>maintenir une bonne hygiène buccodentaire avec des bains de bouche, un gel antiseptique et l'utilisation d’une brosse à
                            dent post-chirurgicale</li>
                        </ul>
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Complications</h2>
                      <p>
                        Bien que tous les efforts soient mis en oeuvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser une pose d'<strong>implant dentaire</strong>, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>le risque anesthésique : bien que rarissime, une allergie est toujours possible</li>
                          <li>la non intégration de l’<strong>implant dentaire</strong> (appelée à tort "rejet") se traduit par la mobilité et la perte de l’implant.
                          Les implants sont en titane  pur, matériau utilisé depuis de nombreuses années pour les prothèses de hanche. Il ne s’agit pas
                          d’une allergie ou de rejet immunitaire, mais de l’absence de "fusion" entre l’os et le titane</li>
                          <li>les infections sont rares. Elles peuvent concerner le voisinage (abcès et péri-implantite), les tissus mous de la face ou
                          de la bouche (cellulite), les sinus (sinusite). Pour éviter ces complications, les implants sont posés dans des conditions
                          d’asepsie rigoureuse et une antibiothérapie post-opératoire est prescrite</li>
                          <li>des hémorragies ou des hématomes peuvent survenir. Dans la très grande majorité des cas, ils sont sans gravité en dehors
                          d’une gêne passagère</li>
                          <li>les lésions des nerfs : cela se traduit par une perte ou diminution de la sensibilité de la lèvre et/ou de la langue (ce
                          n’est pas une paralysie). Le mécanisme est l’atteinte du nerf sensitif qui est dans la mâchoire inférieure. Pour éviter cette
                          complication, un scanner est demandé avant l’intervention afin de localiser le nerf et savoir si la quantité d’os au dessus
                          de celui-ci est suffisante. Malgré les précautions, le nerf peut être atteint lors de la pose (défaut de technique,
                          d’interprétation du scanner, patient bougeant pendant le geste) ou dans les suites du fait d’un œdème ou d’un hématome. Avec
                          un suivi et un traitement adapté, ces troubles sont le plus souvent transitoires (quelques semaines à quelques mois) et
                          exceptionnellement définitifs</li>
                          <li>la péri-implantite et la fracture de l’implant : ces complications tardives peuvent être dues à un manque d’hygiène ou de
                          suivi, une dégradation de l’état général</li>
                          <li>une lésion d’une dent voisine</li>
												</ul>
											</p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Avantages</h2>
                      <p>
                        Les implants permettent la pose d’une prothèse fixe. Ils peuvent remplacer une prothèse amovible partielle ou totale (dentier).
                        Le patient retrouve une mastication améliorée, une élocution plus adaptée et un meilleur confort.<br />
                        En cas de prothèse fixe sur dents naturelles (bridge), les dents adjacentes doivent être "sacrifiées". La pose
                        d’<strong>implant dentaire</strong> évite de traiter les dents avoisinantes saines ou déjà fragilisées (mobilité...).<br />
                        Les implants peuvent également stabiliser une prothèse totale amovible (dentier) grâce à un système d’accroches. Cependant, le
                        patient pourra enlever sa prothèse afin de réaliser les soins d’hygiène quotidiens.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Durée de vie</h2>
                      <p>
                        Après un recul d’une trentaine d’années, nous pouvons affirmer qu’avec une bonne hygiène du patient et un suivi régulier annuel
                        auprès de son praticien et chirurgien, les implants sont une alternative de réhabilitation pouvant durer une dizaine d’années
                        voire plus.
                      </p>
