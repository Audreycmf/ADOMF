											<p>
                        La <strong>blépharoplastie</strong> désigne l'intervention de chirurgie esthétique des paupières qui vise à corriger l’aspect "fatigué" du
                        regard pour une apparence plus reposée et détendue. Elle peut concerner uniquement les deux paupières supérieures ou
                        inférieures, ou encore les quatre paupières à la fois.<br />
                        Une <strong>blépharoplastie</strong> peut être réalisée isolément ou être associée à une autre intervention de chirurgie esthétique du visage
                        (lifting frontal, lifting temporal, lifting cervico-facial).
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Consultation</h2>
                      <p>
                        La consultation permet de distinguer les disgrâces présentes :
                        <ul class="basic-list">
                          <li>paupières supérieures : lourdes et tombantes, avec excès de peau formant un repli ("paupière qui tombe")</li>
                          <li>paupières inférieures : affaissées et flétries avec petites rides horizontales consécutives à la distension cutanée</li>
                          <li>hernies de graisse : responsables de "poche sous les yeux" au niveau des paupières inférieures, de paupières supérieures
                            "bouffies"</li>
                        </ul>
                        L'intervention vise à corriger ces disgrâces sans altérer les fonctions essentielles des paupières. Elle est réalisée aussi
                        bien chez la femme que chez l'homme après 40 ans. Elle est parfois réalisée beaucoup plus précocement dans les cas de disgrâces
                        constitutionnelles (facteurs héréditaires).<br />
                        Un interrogatoire suivi d’un examen des yeux et des paupières est réalisé par le chirurgien à la recherche d’anomalies pouvant
                        compliquer l’intervention, voire la contre-indiquer. Un examen ophtalmologique spécialisé sera fréquemment demandé en complément
                        afin de dépister une éventuelle pathologie oculaire.<br />
                        La consultation permettra également de choisir entre les différents types d’anesthésie. Le choix sera le résultat des impératifs
                        techniques et d'une discussion entre vous, le chirurgien et l'anesthésiste :
												<ul class="basic-list">
													<li>anesthésie locale pure (insensibilité seule des paupières)</li>
													<li>anesthésie vigile (anesthésie locale + tranquillisants administrés par voie intraveineuse)</li>
													<li>anesthésie générale classique</li>
												</ul>
                        L'intervention se pratique en "ambulatoire" (sortie le jour même).<br />
                        Aucun médicament contenant de l’aspirine ne devra être pris dans les 10 jours précédant l’intervention.<br />
                        En fonction du type d’anesthésie, on pourra vous demander de rester à jeun (ne rien manger ni boire ni fumer) 6 heures avant
                        l’intervention.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">L’intervention</h2>
                      <p>
                        Voici les principes de base :
                        <ul class="basic-list">
                          <li>incisions cutanées : elles sont dissimulées dans les plis de la paupière. Les incisions correspondent à l'emplacement des
                            futures cicatrices dans les plis naturels. A noter pour les paupières inférieures, en cas de "poches" isolées (sans excès
                            de peau à enlever), on pourra réaliser une <strong>blépharoplastie</strong> par des incisions placées à l'intérieur des paupières et ne
                            laissant donc aucune cicatrice visible sur la peau</li>
                          <li>résections : les hernies graisseuses sont retirées et l'excédent de muscle et de peau relâché est supprimé</li>
                          <li>sutures : elles sont réalisées avec des fils très fins non résorbables</li>
                        </ul>
                        En fonction de l'ampleur des améliorations à apporter, l'intervention peut durer d’une demi-heure à deux heures.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Suites opératoires</h2>
                      <p>
                        L’intervention est peu douloureuse. Cependant, un inconfort avec une sensation de tension des paupières et une légère irritation
                        des yeux est classique.<br />
                        Les suites opératoires sont marquées par l'apparition d'un &oelig;dème (gonflement) et d'ecchymoses (bleus) dont l'importance et la
                        durée sont individu dépendant. Ces signes sont rapidement réversibles permettant le retour à une vie socioprofessionnelle
                        normale après quelques jours (6 à 20 jours).<br />
                        Les premiers jours, il faut se reposer et éviter les efforts et l’exposition solaire.<br />
                        Les fils sont retirés entre le 6ème et le 10ème jour après l’intervention. Les cicatrices peuvent rester un peu rosées durant
                        les premières semaines mais le maquillage est autorisé.<br />
                        Un délai de 3 à 6 mois est nécessaire pour apprécier le résultat final. Si l’ablation des "poches" est pratiquement définitive,
                        la peau par contre continue à vieillir et se relâcher à la longue. Il est rare qu’une nouvelle intervention soit envisagée
                        avant une douzaine d’années.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Complications</h2>
                      <p>
                        Bien que tous les efforts soient mis en &oelig;uvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser une <strong>blépharoplastie</strong>, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>imperfections de résultat : il ne faut pas confondre imperfections et attente de résultats non raisonnables. Elles
                            résultent de réactions tissulaires inattendues. On peut voir persister une partie des altérations (fines ridules en
                            particulier) ou obtenir un oeil un peu trop creux (rebord osseux de l’orbite visible) ou observer une légère rétraction
                            vers le bas des paupières inférieures, ou encore déceler une petite asymétrie ou des cicatrices un peu trop "blanches". Ces
                            imperfections peuvent être corrigées, le cas échéant, par une petite "retouche" qui se fera la plupart du temps sous simple
                            anesthésie locale, à partir du 6ème mois après l’intervention initiale</li>
                          <li>Complications envisageables :
                            <ol class="level2_list">
                              <li>hématomes : la plupart du temps sans gravité, ils sont évacués s'ils sont trop importants</li>
                              <li>infection : exceptionnelle</li>
                              <li>anomalies de cicatrisation : très rares au niveau des paupières, il peut arriver que les cicatrices ne soient pas, à
                                terme, aussi discrètes qu'escompté</li>
                              <li>kystes épidermiques : ils peuvent apparaître le long des cicatrices, s'éliminent spontanément ou se retirent facilement
                                et ne compromettent pas la qualité du résultat final</li>
                              <li>troubles de la sécrétion des larmes : un larmoiement persistant est plus rare qu'un "syndrome d'&oelig;il sec" qui vient
                                parfois décompenser un déficit en larmes préexistant</li>
                              <li>ptosis (difficulté à ouvrir complètement la paupière supérieure) : très rare, sauf au-delà de 70 ans où un déficit
                                préexistant peut être majoré par l'intervention</li>
                              <li>ectropion (rétraction vers le bas de la paupière inférieure) : il survient parfois sur des paupières peu toniques
                                soumises à une rétraction cicatricielle intempestive; des massages pluriquotidiens permettent de l'estomper après
                                quelques semaines</li>
                              <li>enfin, des cas tout à fait exceptionnels de diplopie (vision double), de glaucome (hypertension oculaire) et même de
                                cécité après <strong>blépharoplastie</strong> ont été rapportés dans la littérature scientifique internationale</li>
                            </ol>
                          </li>
												</ul>
