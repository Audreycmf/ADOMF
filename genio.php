											<header>
												<h2>La génioplastie ou mentoplastie</h2>
											</header>
											<p>
                        La génioplastie ou mentoplastie est une intervention qui a pour but la modification de la forme du menton et éventuellement
                        une  amélioration fonctionnelle (respiration, sangle labiale).<br />
                        Le but est d’obtenir un menton d’aspect naturel s’harmonisant dans ses rapports avec les autres traits du visage,
                        convenant à la psychologie et à la personnalité du patient et répondant aux demandes de ce dernier.<br />
                        L’intervention peut être associée à un autre type d’intervention de chirurgie orthognatique (Cf. fiches correspondantes).
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Consultation</h3>
                      <p>
                        Le praticien va s’enquérir de vos motivations et espérance, examiner votre visage et analyser vos rapports
                        dento-maxillaires. Le praticien pourra, à l’issue de cette analyse globale, vous donner toute l’information utile sur les
                        conséquences de la dysmorphose, sa prise en charge afin de vous guider dans votre prise de décision.<br />
                        Le  praticien vous donnera également quelques recommandations : acquisition d’un jet dentaire, la prise d’aspirine ou d’anti-inflammatoires est déconseillée
                        dans les 10 jours précédents l’intervention. Le tabagisme est également fortement déconseillé.
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">L’intervention</h3>
                      <p>
                        Elle se déroule sous anesthésie générale, le plus souvent lors d’une hospitalisation de 1 à 2 jours.<br />
                        L'incision est dissimulée dans les gencives. Il n’y a aucune incision sur la peau. L’os est découpé précisément (par
                        microscie ou par ultrasons) afin de replacer le menton dans sa position idéale calculée préalablement. L’os est fixé par
                        des mini-plaques et vis en Titane (ostéosynthèse), elles pourront être retirées 1 an après. Les sutures sont réalisées avec des
                        fils résorbables (disparaissenten 3 semaines).<br />
                        Il n’y a pas de blocage des mâchoires en fin d’intervention.<br />
                        L’intervention dure 1 heure. Le soir après l’intervention, le patient peut manger liquide ou très mou.<br />
                        La mâchoire retrouve une solidité normale en 6 semaines.<br />
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Suites opératoires</h3>
                      <p>
                        Les manifestations les plus fréquentes en post-opératoire sont :
                        <ul class="basic-list">
                          <li>un oedème (gonflement) des lèvres et du cou quasi systématique et important la première semaine</li>
                          <li>des petits saignements pendant les 24 premières heures</li>
                          <li>des douleurs modérées, contrôlées par les antalgiques prescrits et disparaissant en général en quelques jours</li>
                          <li>une limitation douloureuse de l'ouverture buccale qui s’estompe en quelques jours</li>
                        </ul>
                        Les soins post-opératoires comportent:
                        <ul class="basic-list">
                          <li>une hygiène bucco-dentaire irréprochable; bains de bouche, jet dentaire</li>
                          <li>des médicaments contre la douleur (antalgiques)</li>
                          <li>une vessie de glace (effets antalgique, anti-inflammatoire et anti-oedémateux)</li>
                          <li>des antibiotiques</li>
                          <li>une alimentation molle pendant 1 mois</li>
                          <li>un arrêt du tabac pendant la période postopératoire</li>
                        </ul>
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Complications</h3>
                      <p>
                        Bien que tous les efforts soient mis en oeuvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser une ostéotomie de Le Fort, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>Complications hémorragiques :<br />
                            Bien que rares, elles peuvent nécessiter une transfusion sanguine, voire une réintervention chirurgicale.
                          </li>
                          <li>Complications nerveuses :<br />
                            En post-opératoire immédiat, une perte de la sensibilité totale (anesthésie) ou partielle (hypoesthésie) labio-mentonnière inférieure est
                            classique. Elle est liée à la présence du nerf alvéolaire inférieur à droite et à gauche proche de la zone opératoire. Ces troubles de la
                            sensibilité sont temporaires et récupèrent en quelques mois dans la grande majorité des cas. La persistance exceptionnelle de ces
                            troubles est une séquelle.
                          </li>
                          <li>Complications infectieuses :<br />
                            Elles se manifestent par un abcès du menton. La mise en route d’un traitement antibiotique est nécessaire,
                            avec parfois un drainage chirurgical complémentaire.
                          </li>
                          <li>Complications dentaires :<br />
                            Parfois, les racines dentaires peuvent souffrir. Des soins dentaires seront indiqués.
                          </li>
                          <li>Complications osseuses :
                            <ul class="level2_list">
                              <li>Fonte du fragment osseux. Une reconstruction secondaire nécessitera alors une greffe osseuse ou la mise en
                                place d’un implant mentonnier.</li>
                              <li>L’absence de consolidation osseuse (pseudarthrose) est très exceptionnelle au niveau de la mandibule. Elle nécessite
                                une intervention avec greffes osseuses et une nouvelle ostéosynthèse.</li>
                            </ul>
                          </li>
												</ul>
