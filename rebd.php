                    <section>
                      <header>
                        <h2>Les extractions dentaires difficiles</h2>
                      </header>
                      <p>
                        Le chirurgien maxillo-facial peut réaliser les extractions dentaires difficiles :
                        <ul class="basic-list">
                          <li>cas de dent dévitalisée et ankylosée</li>
                          <li>cas d'extractions de dents multiples</li>
                          <li>présence de kystes plus ou moins volumineux attenant aux dents</li>
                          <li>risques de complications hémorragiques du fait de traitement anti-agrégant (Aspégic&reg;, Kardégic&reg;, Plavix&reg; Efient&reg;)
                            ou anticoagulant (Préviscan&reg;, Sintron&reg;, Pradaxa&reg;, Xarelto&reg;, Eliquis&reg;)</li>
                          <li>risques de complications ostéonécrotiques du fait de la prise de biphosphonates (Didronel&reg;, Clastoban&reg;, Fosamax&reg;,
                            Actonel&reg;, Zometa&reg;, Aclasta&reg;, Xgeva&reg;) ou d'antécédent de radiothérapie</li>
                        </ul>
                        Ces différentes conditions sont bien souvent intriquées les unes aux autres.
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Consultation</h3>
                      <p>
                        Le chirurgien vous donnera une information objective et compréhensible sur les modalités de votre prise en charge, les risques  et  aléas
                        encourus à être opéré. Celle-ci doit vous permettre de mieux comprendre la chirurgie ou le geste qui vous est proposé. N’hésitez pas à
                        poser les questions que vous souhaitez à votre chirurgien.
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">L’intervention</h3>
                      <p>
                        L'intervention se déroule sous anesthésie locale ou générale en fonction du nombre de dents à extraire, des conditions particulières
                        (allergie, phobie...).<br />
                        Quelle que soit le mode d’anesthésie, la technique chirurgicale reste la même. La gencive est parfois suturée à l’aide de fils
                        résorbables (disparaissant seuls en un mois environ).<br />
                        En cas de traitement anti-agrégant ou anticoagulant, un produit hémostatique (qui limite le saignement) est mis en place sur les gencives.
                        Il se résorbe rapidement.<br />
                        La durée de l’intervention est très variable en fonction des difficultés techniques.
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Suites opératoires</h3>
                      <p>
                        Les manifestations les plus fréquentes en post-opératoire sont :
                        <ul class="basic-list">
                          <li>des petits saignements pendant les 24 premières heures</li>
                          <li>des douleurs contrôlées par les antalgiques prescrits et disparaîssant en général en quelques jours</li>
                          <li>un oedème des joues : imprévisible et variable d’une personne à l’autre</li>
                          <li>une limitation douloureuse de l'ouverture buccale qui s’estompe en quelques jours</li>
                        </ul>
                        Les soins post-opératoires comportent:
                        <ul class="basic-list">
                          <li>des bains de bouche, à débuter seulement 24 heures après l’intervention</li>
                          <li>des médicaments contre la douleur (antalgiques)</li>
                          <li>une vessie de glace pendant 24 heures (effets antalgique, anti-inflammatoire et anti-oedémateux)</li>
                          <li>parfois des antibiotiques et anti-inflammatoires</li>
                          <li>une alimentation molle, tiède ou froide pendant les 24 premières heures</li>
                          <li>un arrêt du tabac pendant la période postopératoire</li>
                        </ul>
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Complications</h3>
                      <p>
                        Bien que tous les efforts soient mis en oeuvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser l’extraction des dents de sagesse, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>Complication infectieuse :
                            <ul class="level2_list">
                              <li>L'alvéolite (infection de la cavité d’extraction) peut survenir, le plus souvent à la troisième semaine post-opératoire. Elle
                                est provoquée par des débris alimentaires dans la cavité d’extraction. La mise en route d’un traitement antibiotique associé à un
                                lavage sous anesthésie locale est alors nécessaire. Une intervention est exceptionnelle.</li>
                            </ul>
                          </li>
                          <li>Autres complications :
                            <ul class="level2_list">
                              <li>L'alvéolite sèche survient quelques jours après l’intervention. C’est un retard de cicatrisation, fortement favorisé par le
                                tabagisme. Elle nécessite souvent un traitement local.</li>
                              <li>Des gerçures des lèvres, de la face interne des joues sont des complications bénignes. Elles régressent en quelques jours.</li>
                              <li>Des hémorragies (saignements importants) peuvent survenir et sont résolues par des manoeuvres simples de compression. Ce type
                                de complication concerne principalement les patients sous traitement anti-agrégant ou anticoagulant ou présentant des pathologies
                                particulières qui favorisent les hémorragies.</li>
                            </ul>
                          </li>
                          <li>Complications dentaires :
                            <ul class="level2_list">
                              <li>La perte d’un plombage ou le descellement d’une couronne sont possibles.</li>
                              <li>La persistance d’un fragment de racine.Il est parfois préférable de laisser en place un bout de racine difficile à extraire
                                trop proche du nerf alvéolaire inférieur. Il n’y a aucune suite dans la plupart des cas.</li>
                            </ul>
                          </li>
                          <li>Complications sinusiennes:<br />
                            Elles concernent exclusivement les dents de sagesse du haut qui sont proches des sinus maxillaires.
                            <ul class="level2_list">
                              <li>La communication bucco-sinusienne. Elle se ferme spontanément dans la plupart des cas. Mais sa persistance peut nécessiter un
                                traitement chirurgical.</li>
                              <li>Une migration anormale de la dent dans le sinus maxillaire peut survenir. Cette complication est rare. La dent doit être
                                récupérée afin d'éviter l'apparition d'une sinusite.</li>
                            </ul>
                          </li>
                          <li>Complication osseuse :
                            <ul class="level2_list">
                              <li>La fracture de la mâchoire inférieure reste exceptionnelle.</li>
                            </ul>
                          </li>
                          <li>Complications nerveuses :
                            <ul class="level2_list">
                              <li>Elles surviennent en cas de particularités anatomiques. Elles se manifestent par des troubles de la sensibilité (totale =
                                anesthésie ou partielle = hypoesthésie). Elles sont le plus souvent transitoires.</li>
                            </ul>
                          </li>
												</ul>
                      </p>
