                      <p>
                        Les kystes dentaires se développent à la pointe de la racine des dents mortifiées. Il s’agit d’une lésion infectieuse chronique
                        évoluant lentement et à bas bruit.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Consultation</h2>
                      <p>
                        La consultation peut survenir dans les cas suivant :
                        <ul class="basic-list">
                          <li>le kyste dentaire est responsable d’un abcès ou de manifestations infectieuses à répétition</li>
                          <li>le patient présente une pathologie ou un état de santé (diabète, prothèse valvulaire cardiaque, prothèse de hanche...) nécessitant
                            l’éradication de tout foyer infectieux risquant d’aggraver son état</li>
                          <li>risque de complications comme les sinusites</li>
                        </ul>
                        Pour résoudre le problème des kystes apicaux, deux solutions thérapeutiques sont envisageables :
                        <ul class="basic-list">
                          <li>l'avulsion de la dent et de son kyste (solution radicale)</li>
                          <li>la <strong>résection apicale</strong> permettant de conserver la dent (risque de récidive)</li>
                        </ul>
                        Le choix entre les deux possibilités thérapeutiques dépend du volume du kyste et de sa situation anatomique (en particulier la relation
                        avec le sinus maxillaire), et de l’état général du patient. Le chirurgien maxillo-facial vous expliquera la solution la plus adaptée à votre
                        cas.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">L’intervention</h2>
                      <p>
                        La <strong>résection apicale</strong> consiste à supprimer le kyste dentaire et seulement l’extrémité de la racine dentaire. Elle se déroule sous
                        anesthésie locale la plupart du temps mais peut parfois bénéficier d’une anesthésie générale. La gencive est suturée avec du fil
                        résorbable (disparaît spontanément en 3 semaines).<br />
                        Cette technique nécessite absolument que la dent à traiter soit dévitalisée. Si ce n'est pas le cas, il faudra coordonner les soins avec
                        votre dentiste afin qu'il procède au traitement canalaire de la dent. Idéalement la dévitalisation de la dent sera réalisée avant la
                        résection.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Suites opératoires</h2>
                      <p>
                        Les manifestations les plus fréquentes en post-opératoire sont :
                        <ul class="basic-list">
                          <li>des petits saignements pendant les 24 premières heures</li>
                          <li>des douleurs contrôlées par les antalgiques prescrits et disparaissant en général en quelques jours</li>
                          <li>un &oelig;dème des joues : imprévisible et variable d’une personne à l’autre</li>
                        </ul>
                        Votre ordonnance comprendra :
                        <ul class="basic-list">
                          <li>des bains de bouche, à débuter seulement 24 heures après l’intervention</li>
                          <li>des médicaments contre la douleur (antalgiques) et/ou des anti-inflammatoires</li>
                          <li>une vessie de glace sur la joue pendant 24 heures (effets antalgique, anti-inflammatoire et anti-&oelig;démateux)</li>
                          <li>des antibiotiques</li>
                          <li>une alimentation molle, tiède ou froide pendant les 24 premières heures. Ceci diminue le risque de saignements</li>
                          <li>un arrêt du tabac pendant la période postopératoire. Le tabac favorise les complications infectieuses et une mauvaise cicatrisation</li>
                        </ul>
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Complications</h2>
                      <p>
                        Bien que tous les efforts soient mis en &oelig;uvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser une <strong>résection apicale</strong>, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>Complications hémorragiques :
														<ul class="level2_list">
                            	<li>des saignements importants peuvent survenir. Ils cèdent en mordant des compresses, et dans de rares cas, un petit geste
															sous anesthésie locale et nécessaire</li>
														</ul>
                          </li>
                          <li>Complication infectieuse (cellulite) :
														<ul class="level2_list">
	                            <li>elle peut survenir quelques jours à quelques semaines après la <strong>résection apicale</strong>. Un traitement antibiotique associé ou non à un geste sous anesthésie locale
	                            est mis en place. Une réintervention sous anesthésie générale est rare. L’infection de l’os (ostéite) est très exceptionnelle</li>
														</ul>
                          </li>
                          <li>Récidive du kyste :
														<ul class="level2_list">
	                            <li>l'infection dentaire persiste malgré l'ablation du kyste et justifie alors l'avulsion dentaire</li>
														</ul>
                          </li>
                          <li>Complications sinusiennes :
														<ul class="level2_list">
	                            <li>elles concernent exclusivement les résections apicales sur les dents du haut qui sont en relation étroite avec les sinus maxillaires.
	                            La <strong>résection apicale</strong> peut provoquer une sinusite maxillaire</li>
														</ul>
                          </li>
                          <li>Complications nerveuses :
														<ul class="level2_list">
	                            <li>le nerf alvéolaire inférieur (droit et gauche) est responsable de la sensibilité labio-mentonnière du même côté. Sa proximité avec
	                            les racines de la dent peut être source de souffrance se manifestant par une perte de la sensibilité  totale (anesthésie) ou
	                            partielle (hypoesthésie) labio-mentonnière. Ces troubles de la sensibilité sont temporaires et récupèrent en quelques semaines dans
	                            la grande majorité des cas</li>
														</ul>
                          </li>
												</ul>
                      </p>
