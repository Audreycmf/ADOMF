											<p>
                        En cas de décalage ou d’anomalie importante des mâchoires, et quand la croissance est terminée, il est indispensable d’associer
                        une correction chirurgicale au traitement d’orthodontie.
                      </p>
                      <p>
                        L’objectif final de ce traitement orthodontico-chirurgical (ou traitement mixte) est l’obtention de l’optimum fonctionnel et
                        esthétique ainsi qu’un traitement stable.<br />
                        Dans ce cas, l’orthodontie vise à corriger les malpositions dentaires, idéaliser la position des dents et rendre les deux
                        mâchoires aptes à s’emboîter dans la position corrigée chirurgicalement.
                      </p>
                      <p>
                        Le traitement nécessite la pose de bagues, qui peuvent être métalliques, céramiques transparentes ou posées sur la face
                        interne des dents (non visibles) dénommé traitement lingual pour une discrétion totale.<br />
                        Selon votre besoin, des appareils fixes ou amovibles sont à prévoir. Parfois, certains traitements exigent le port des deux
                        types d'appareils. Lors de votre bilan orthodontique, il sera déterminé quel appareil vous convient. L'orthodontiste est le
                        spécialiste le plus qualifié pour vous conseiller. L'appareil qu'il vous indiquera sera le plus adapté afin que le traitement
                        soit efficace et les résultats durables.
                      </p>
                      <p>
                        Dans la très grande majorité des cas ces traitements se déroulent en 3 étapes :
                        <ul class="basic-list">
                          <li>préparation orthodontique</li>
                          <li>chirurgie des mâchoires</li>
                          <li>finitions orthodontiques</li>
                        </ul>
                        Le patient doit rester bagué lors de la <strong>chirurgie orthognatique</strong> pour la consolidation du traitement.<br />
                        Les finitions permettent de corriger les dernières imperfections et obtenir ainsi un emboîtement parfait.
