											<p>
                        La <strong>toxine botulique</strong> a des propriétés de relaxation musculaire. Son emploi permet de réduire l’action des muscles et ainsi
                        d’atténuer les rides d’expression pour obtenir ainsi un effet de rajeunissement.<br />
                        En pratique, les indications les plus classiques de la <strong>toxine botulique</strong> dans le domaine de l’esthétique concernent les rides
                        inter-sourcilières (ride du lion), les rides du front et les rides de la patte d’oie.<br />
                        En France depuis 2003, seuls les chirurgiens maxillo-faciaux, les chirurgiens plasticiens, les ophtalmologues et les
                        dermatologues sont autorisés à utiliser la toxine à titre esthétique.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Consultation</h2>
                      <p>
                        Lors de la consultation, le praticien va rechercher les contre-indications à l’utilisation de la <strong>toxine botulique</strong> :
                        <ul class="basic-list">
                          <li>maladies neuromusculaires (sclérose en plaque, myasthénie…)</li>
                          <li>grossesse et allaitement</li>
                          <li>traitement antibiotique en cours de la famille des aminosides</li>
                          <li>hypersensibilité connue à l’albumine ou à la neurotoxine botulinique</li>
                        </ul>
                        Une fois éliminées ces contre-indications, il vous donnera quelques recommandations dans la perspective d’une séance
                        d’injection de <strong>toxine botulique</strong> :
                        <ul class="basic-list">
                          <li>éviter la prise d’aspirine ou d’anti-inflammatoire dans la semaine précédente pour limiter le risque d’hématome</li>
                          <li>venir démaquiller</li>
                        </ul>
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">L’intervention</h2>
                      <p>
                        La séance se déroule en cabinet. Il n’y a pas besoin d’être à jeun. Les injections de <strong>toxine botulique</strong> ne nécessitent pas d’anesthésie locale.
                        Quelques recommandations sont à respecter après la séance afin d’éviter un résultat aléatoire :
                        <ul class="basic-list">
                          <li>ne pas masser les zones injectées pendant 24h</li>
                          <li>pas de pratique sportive intensive le jour même</li>
                          <li>pas d’exposition solaire pendant 24h</li>
                        </ul>
                        Le résultat apparaît entre le 4ème et 8ème jour et se stabilise à la 3ème semaine. Il perdure entre 4 et 6 mois lors d’une
                        première injection et s’allonge en répétant les injections.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Suites opératoires</h2>
                      <p>
                        Des effets indésirables peuvent apparaître transitoirement : ecchymoses ("bleus") au point d’injection, troubles de la
                        sensibilité (sensation de tension, de cartonnement). Il ne faut pas hésiter à contacter son praticien si vous avez le moindre
                        doute.<br />
                        Les imperfections de résultat (insuffisance, asymétrie…) sont souvent dus à l’asymétrie préexistante  des muscles. Ces
                        imperfections sont corrigées par une injection complémentaire de <strong>toxine botulique</strong> à la 4ème semaine.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Complications</h2>
                      <p>
                        Bien que tous les efforts soient mis en &oelig;uvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser une injection de <strong>toxine botulique</strong>, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Les complications sont très rares, parfois gênantes et transitoires :
