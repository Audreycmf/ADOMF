											<p>
                        La maladie de Basedow est une hyperthyroïdie auto-immune provoquée par des anticorps circulants stimulant la thyroïde dénommés
                        anticorps anti-récepteurs à la TSH (TRAK). La maladie touche principalement la femme jeune dans la seconde ou la troisième
                        décade de vie.<br />
                        Les patients atteints de la maladie de Basedow peuvent développer ce que l’on appelle une &laquo;orbitopathie dysthyroïdienne&raquo;.
                        Celle-ci va se traduire par une exophtalmie parfois importante.<br />
                        Cette exophtalmie est due à une infiltration des muscles de l’œil et/ou de la graisse orbitaire ce qui entraîne une augmentation
                        de volume intra-orbitaire. Une diplopie (vision double)et/ou une rétraction des paupières (inférieures, supérieures ou les deux)
                        peuvent également être présents.<br />
                        La prise en charge de l’orbitopathie dysthyroïdienne est pluridisciplinaire (endocrinologue, ORL, ophtalmologiste, chirurgien
                        maxillo-facial). Elle se fait par 1, 2 ou 3 étapes chirurgicales et selon un calendrier précis :
                        <ul class="basic-list">
                          <li>1ère étape : <a href="decompression_orbitaire.php">décompression orbitaire</a> proprement dite. Elle ne peut avoir lieu
                            qu'après au moins 6 mois de stabilité de la maladie thyroïdienne</li>
                          <li>2ème étape (non systématique) : la chirurgie oculomotrice si le patient voit double après la décompression. Elle se fait
                          4 à 6 mois après la décompression. Elle est effectuée par un ophtalmologiste</li>
                          <li>3ème étape (non systématique) : <a href="allongement_palpebral.php">chirurgie des paupières rétractées</a>. Si elle est
