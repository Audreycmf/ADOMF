											<h1>Qu'est-ce que la chirurgie maxillo-faciale ?</h1>
											<p>
												Les Deux guerres mondiales du XXème siècle ont été pourvoyeuses d'un grand nombre de traumatismes faciaux majeurs à l'origine
												des célèbres "Gueules cassées". Un blessé qui survivait était toujours défiguré. Certains chirurgiens se sont alors réellement
												penchés sur l'art de la chirurgie plastique et reconstructrice de la face. Ils se sont spécialisés dans la chirurgie du
												visage : la chirurgie <strong>maxillo-faciale</strong> était née. </br>
												Dès le départ, ces chirurgiens avaient compris qu'il était inconcevable de reconstruire un visage sans connaître parfaitement
												les dents et les maxillaires, clés de voûte de l'architecture faciale, point de départ de toute reconstruction. Chirurgie <strong>maxillo-faciale</strong> et
												stomatologie sont depuis indissociables. <br />
												Aujourd'hui les chirurgiens maxillo-faciaux français sont issus d'une très longue formation : études de médecine, internat,
												formation en chirurgie générale, puis spécialisation en chirurgie <strong>maxillo-faciale</strong>, soit 13 ans d'études au minimum. <br />
												Un chirurgien <strong>maxillo-facial</strong> est donc un spécialiste de la Chirurgie de la Face. Son diplôme, garant d'une compétence en chirurgie
												plastique de la face, est reconnu au même titre que celui de chirurgie plastique reconstructrice et esthétique par la
												société française de chirurgie plastique reconstructrice et esthétique et lui permet donc d'adhérer à cette Société.

												<h2>Ses compétences officielles couvrent :</h2>
													<ul class="check-list">
														<li>la chirurgie plastique, la chirurgie reconstructrice et la chirurgie esthétique de la face</li>
														<li>la  chirurgie orthognathique (problèmes  d’occlusion dentaire  en  relation  avec  des  anomalies  de  taille  ou  de  rapport  des
														maxillaires)</li>
														<li>le traitement des malformations de la face de l'enfant et de l'adulte</li>
														<li>la traumatologie faciale</li>
														<li>la stomatologie (notamment : dents de sagesse, kystes, dents incluses, implants, chirurgie pré-prothétique,etc)</li>
														<li>la chirurgie des cancers de la bouche et de la face</li>
														<li>la chirurgie des glandes salivaires</li>
														<li>la chirurgie orbito-palpébrale</li>
													</ul>
												<h2>Les chirurgiens maxillo-faciaux travaillent souvent en étroite collaboration :</h2>
													<ul class="basic-list">
														<li>avec les orthodontistes (chirurgie orthognathique)</li>
														<li>avec les dentistes (extraction de dents de sagesse, kystes, greffe osseuse, mise en place d'implants)</li>
														<li>avec les dermatologues (notamment exérèse de lésions cutanées du visage)</li>
														<li>avec les ophtalmologues et les endocrinologues (chirurgie des paupières et de l'orbite)</li>
														<li>avec les pneumologues (syndrome d'apnées obstructives du sommeil)</li>
														<li>avec les neurochirurgiens (tumeurs ou grands traumatismes touchant à la fois le visage et le crâne)</li>
													</ul>

												Pourtant  cette  spécialité  est  peu  médiatisée  et  donc  peu  connue  car  les  spécialistes  sont  peu  nombreux.  Certains  praticiens
												non diplômés en chirurgie <strong>maxillo-faciale</strong> profitent de l'ignorance du public pour réaliser des actes chirurgicaux pour lesquels ils n'ont
												pas été correctement formés. </br>
												Les chirurgiens maxillo-faciaux  ont  été  reçus  à  l'internat  des  spécialités  chirurgicales.  Ils  ont  ensuite  reçu  une formation
												de 2 années de chirurgie générale, puis 5 ans de chirurgie <strong>maxillo-faciale</strong> et de stomatologie. Titulaires d'un "Diplôme
												d'Etudes  Spécialisées  en  Chirurgie  <strong>maxillo-faciale</strong>  et  stomatologie",  ils  harmonisent  la  chirurgie  osseuse et  la chirurgie
												plastique et esthétique du visage, afin de prendre en charge dans sa globalité les différentes pathologies de la face : les cancers des
												muqueuses  aérodigestives  supérieures,  les  tumeurs  cutanées  de  la  face,  les  traumatismes  de  la  face,  les  pathologies  des
												glandes salivaires, les malformations faciales de l'enfant et de l'adulte, l'équilibration de la fonction manducatrice (mastication,
												salivation, déglutition, etc.), les pathologies des paupières et de l'orbite (dans la maladie de Basedow par exemple), la réhabilitation de la cavité buccale (éradication des foyers infectieux, exérèse de tumeurs, reconstruction
												et/ou adaptation d’une prothèse <strong>maxillo-faciale</strong>, etc.), et la stomatologie (kystes, dents de sagesse, chirurgie pré-implantaire, implantaire,
												etc.). <br />
												Enfin  sachez  que  la  chirurgie  esthétique  <strong>maxillo-faciale</strong>  est  clairement  inscrite  dans  la  maquette  de  formation  du  Chirurgien
												Maxillo-facial, publiée au Bulletin Officiel du Ministère de l'Education Nationale. <br />
												Pour  vous  assurer  que  le  chirurgien  que  vous  allez  consulter  est  bien  un  chirurgien  <strong>Maxillo-facial</strong>,  consultez  le  site  du
												Conseil National de l'Ordre des Médecins ou téléphonez au Conseil Départemental de l'Ordre des Médecins de votre département.
											</p>
