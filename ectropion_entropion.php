											<p>
                        Les troubles de la statique palpébrale se traduisent par une malposition de la paupière, en particulier du bord libre qui
                        comporte les cils.<br />
                        Dans l'<strong>entropion</strong>, ce bord libre se retourne vers l'œil et les cils peuvent venir frotter la cornée.<br />
                        Dans l'<strong>ectropion</strong>, c'est le phénomène inverse : le bord de la paupière s'éverse vers l'extérieur et perd contact avec le globe
                        oculaire. Il s'ensuit une kératinisation, c'est-à-dire un assèchement de la face oculaire de la paupière avec rougeur. Le globe
                        oculaire est mal protégé et la cornée peut s'ulcérer. L'éversion peut toucher le point lacrymal en dedans et de ce fait
                        entraîner un larmoiement.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Consultation</h2>
                      <p>
                        Elle va déterminer l’origine de votre <strong>entropion</strong>/<strong>ectropion</strong> (congénital, sénile, spasmodique, cicatriciel, paralytique).<br />
                        Nous testerons la laxité horizontale et verticale et évaluerons la qualité de la peau.<br />
                        L’ensemble de ces éléments nous permet de choisir la technique opératoire la plus adaptée à votre cas.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">L’intervention</h2>
                      <p>
                        Cette intervention peut se faire sous anesthésie générale ou le plus souvent sous anesthésie locale, éventuellement
                        potentialisée par l'anesthésiste (neuroleptanalgésie), au décours d’une hospitalisation d’une ou 2 journées.<br />
                        La chirurgie vise à retendre la paupière inférieure. Le plus souvent, cette intervention comporte une résection de tissu cutané
                        et du muscle de la paupière incriminée qui est le muscle orbiculaire. C'est le cas de la tarsorraphie externe où l'on tire la paupière
												vers l'extérieur. Elle sera fixée dans cette position et l'excès sera retiré. Dans certains cas, elle nécessite la résection d'un
                        morceau de paupière sur toute son épaisseur.<br />
												En cas d'<strong>ectropion</strong> isolé du point lacrymal, on pourra essayer de le repositionner en ne retirant qu'une partie de la conjonctive et du
												tarse sous-jacent qui se trouve sur la face interne de la paupière.<br />
												En cas d'<strong>ectropion</strong> cicatriciel, une greffe de peau sera nécessaire. Une greffe conjonctivale sera réalisée dans le cas d'un <strong>entropion</strong>
												cicatriciel.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Suites opératoires</h2>
                      <p>
                        Elles sont simples. Elle nécessitent :
                        <ul class="basic-list">
                          <li>des compresses oculaires avec de l’eau glacée dans les heures suivant l’intervention pour prévenir la survenue d’hématome</li>
                          <li>une pommade sur les yeux et la cicatrice pendant une semaine</li>
                        </ul>
                        L’ablation des fils se fait entre le 7ème et le 10ème jour.<br />
                        L’ecchymose et l’œdème peuvent persister jusqu’à 10 jours.
                      </p>
