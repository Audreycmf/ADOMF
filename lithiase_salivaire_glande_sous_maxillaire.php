                        La sous-maxillectomie est l'ablation de la glande sous-maxillaire. La glande sous-maxillaire est une des glandes salivaires
                        principales. Nous en avons deux, une à droite et une à gauche sous le bord de la mâchoire.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Consultation</h2>
                      <p>
                        Les pathologies rencontrées au niveau de la glande sous-maxillaire sont les mêmes qu’au niveau de la parotide avec une
                        distribution différente :
                        <ul class="basic-list">
                          <li>pathologies inflammatoires voire infection chronique souvent liées à une <strong>lithiase salivaire</strong> (calcul). Elles sont les plus
                            fréquentes</li>
                          <li>pathologie tumorale de nature bénigne (70% des cas) ou maligne</li>
                        </ul>
                        En cas d’échec d’un traitement médical ou du fait de la nature de la pathologie, le chirurgien maxillo-facial peut proposer
                        l’ablation de la glande sous-maxillaire et de la <strong>lithiase salivaire</strong>.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">L’intervention</h2>
                      <p>
                        L'intervention se déroule sous anesthésie générale et donc en hospitalisation.<br />
                        L’incision de quelques centimètres se situe dans un pli cutané du cou. En cas de tumeur, elle sera analysée pendant
                        l'intervention. C’est l’examen anatomopathologique extemporané qui permet de connaître la nature de la lésion pendant
                        l’intervention. Le diagnostic sera confirmé par l’examen anatomopathologique définitif en post-opératoire. En cas de tumeur
                        maligne, il est souvent nécessaire de prélever les ganglions situés autour de la glande pour les faire analyser. Il faut alors
                        souvent agrandir l’incision pour effectuer un curage ganglionnaire. En cas de tumeur bénigne, il n’y a pas de geste
                        complémentaire. En cas de <strong>lithiase salivaire</strong>, on s'assure de l'absence de <strong>lithiase salivaire</strong> dans le canal évacuateur de la glande. Il est parfois
                        nécessaire de faire une petite incision par voie endo-buccale, sous la langue.<br />
                        En fin d’intervention, un drain peut être mis en place pour évacuer l’hématome post-opératoire. Il sera retiré le jour de la sortie.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Suites opératoires</h2>
                      <p>
                        La durée de l’hospitalisation est de 2 ou 3 jours.<br />
                        Les suites opératoires sont peu douloureuses et très bien contrôlées par des antalgiques simples.<br />
                        Des soins locaux sont réalisés jusqu’à l’ablation des points entre le 10ème et le 15ème jour post-opératoire.<br />
                        La cicatrice s’estompe après quelques mois pour se fondre dans les plis du cou. Il peut persister une légère dépression sous le
                        rebord de la mâchoire.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Complications</h2>
                      <p>
                        Bien que tous les efforts soient mis en &oelig;uvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser l'ablation de la glande sous-maxillaire, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>les hémorragies et surinfections sont exceptionnelles</li>
                          <li>la parésie (paralysie incomplète) de la lèvre inférieure du côté opéré. Elle se manifeste par une asymétrie du sourire et
                            lors de la parole. Elle est due à une "paresse" du rameau mentonnier du nerf facial suite à la dissection de la région
                            opérée. Cette asymétrie est temporaire et récupère en quelques semaines (spontanément ou aidée par des séances de
                            kinésithérapie)</li>
                          <li>les cicatrices hypertrophiques ou véritables chéloïdes sont exceptionnelles au niveau de cette région. Elles nécessitent
                            des soins complémentaires</li>
                          <li>les complications exceptionnelles surviennent lors d’une chirurgie techniquement complexe (tumeur inflammatoire ou
                            infectée, seconde intervention, tumeur maligne) :
                            <ol class="level2_list">
                              <li>un traumatisme du nerf grand hypoglosse (responsable de la mobilité de l’hémi-langue du côté opéré). Il se manifeste
                                par une paralysie et atrophie de l’hémi-langue. En pratique, les conséquences sont mineures. L’hémi-langue controlatérale
                                compense la déglutition et la parole</li>
                              <li>un traumatisme du nerf lingual (responsable de la sensibilité de l’hémi-langue du côté opéré). Le manque de
                                sensibilité de langue du côté opéré est plus ou moins gênant</li>
                            </ol>
                          </li>
