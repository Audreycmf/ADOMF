											<p>
                        Les 4 dents de sagesse (ou troisième molaire) sortent normalement à la fin de l’adolescence. Parfois leur nombre est inférieur. Lorsqu'une
                        <strong>dent de sagesse</strong> n'est pas sortie correctement sur l’arcade, elle est dite enclavée (à moitié sortie) ou incluse (bloquée
                        dans l’os).
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Consultation</h2>
                      <p>
                        L’indication d’extraction de <strong>dent de sagesse</strong> peut être posées dans les situations suivantes :
                        <ul class="basic-list">
                          <li>péricoronarites récidivantes : douleurs et/ou surinfections. C’est souvent le cas pour une <strong>dent de sagesse</strong> enclavée</li>
                          <li>après un traitement orthodontique : la <strong>dent de sagesse</strong> n’aura pas de place suffisante pour sortir et risquerait de perturber
                            le bon alignement dentaire obtenu par le traitement orthodontique, donc d’en perdre le bénéfice</li>
                        </ul>
                        La  consultation permettra de déterminer les modalités de l'extraction de <strong>dent de sagesse</strong> et d’expliquer celles-ci, d’évaluer si vous présentez des
                        spécificités. En particulier, la radiographie panoramique montre les rapports entre le nerf sensitif du menton (nerf alvéolaire inférieure) et les racines de la dent
                        de sagesse du bas. Il peut exister un risque de lésion de ce nerf. Votre chirurgien maxillo-facial pourra donc vous préciser si vous êtes
                        plus susceptible qu’un autre de faire ce type de complication. Au besoin il vous prescrira un dentascanner (scanner des dents et des
                        mâchoires) avant l’intervention pour connaître précisément les rapports anatomiques entre le nerf et la <strong>dent de sagesse</strong> afin de limiter le risque de
                        complication.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">L’intervention</h2>
                      <p>
                        L’extraction de <strong>dent de sagesse</strong> peut se pratiquer :
                        <ul class="basic-list">
                          <li>sous anesthésie locale. L’anesthésie ne peut être réalisée sur la mâchoire inférieure simultanément à gauche et à droite car
                            dangereux (risque de troubles de la déglutition) : il faut donc envisager une séance pour les dents de sagesse haut et bas d’un côté
                            et une deuxième séance espacée d’un mois environ pour l’autre côté.</li>
                          <li>sous anesthésie générale. Une consultation d’anesthésie est indispensable. L’intervention se déroule lors d’une hospitalisation sur
                            la journée (hospitalisation ambulatoire).</li>
                        </ul>
                        Quelque soit le type d’anesthésie, la technique est identique. La durée de l’intervention varie en fonction des difficultés entre 5 et 10
                        minutes par <strong>dent de sagesse</strong>. La gencive inférieure est suturée par un fil résorbable (qui disparaît seul en 3 semaines environ). La gencive
                        supérieure ne nécessite pas de suture le plus souvent.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Suites opératoires</h2>
                      <p>
                        Les manifestations les plus fréquentes en post-opératoire sont :
                        <ul class="basic-list">
                          <li>des petits saignements pendant les 24 premières heures</li>
                          <li>des douleurs contrôlées par les antalgiques prescrits et disparaîssant en général en quelques jours</li>
                          <li>un &oelig;dème des joues : imprévisible et variable d’une personne à l’autre</li>
                          <li>une limitation douloureuse de l'ouverture buccale qui s’estompe en quelques jours</li>
                        </ul>
                        Les soins post-opératoires comportent:
                        <ul class="basic-list">
                          <li>des bains de bouche, à débuter seulement 24 heures après l’intervention</li>
                          <li>des médicaments contre la douleur (antalgiques)</li>
                          <li>une vessie de glace pendant 24 heures (effets antalgique, anti-inflammatoire et anti-&oelig;démateux)</li>
                          <li>parfois des antibiotiques et anti-inflammatoires</li>
                          <li>une alimentation molle, tiède ou froide pendant les 24 premières heures</li>
                          <li>un arrêt du tabac pendant la période postopératoire</li>
                        </ul>
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Complications</h2>
                      <p>
                        Bien que tous les efforts soient mis en &oelig;uvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser l’extraction de <strong>dent de sagesse</strong>, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>Complications nerveuses :
                            <ul class="level2_list">
                              <li>le nerf alvéolaire inférieur (droit et gauche) est responsable de la sensibilité labio-mentonnière du même côté. Sa proximité
                                avec les racines de la <strong>dent de sagesse</strong> inférieure peut être source de souffrance se manifestant par une perte de la sensibilité
                                totale (anesthésie) ou partielle (hypoesthésie) labio-mentonnière. Ces troubles de la sensibilité sont temporaires et récupèrent
                                en quelques semaines dans la grande majorité des cas</li>
                              <li>le nerf lingual est également parfois à proximité de la <strong>dent de sagesse</strong> du bas. Les mêmes troubles se manifestent au niveau de
                                la langue avec le même pronostic</li>
                            </ul>
                          </li>
                          <li>Complication infectieuse :
                            <ul class="level2_list">
                              <li>l'alvéolite (infection de la cavité d’extraction) peut survenir, le plus souvent à la troisième semaine post-opératoire. Elle
                                est provoquée par des débris alimentaires dans la cavité d’extraction. La mise en route d’un traitement antibiotique associé à un
                                lavage sous anesthésie locale est alors nécessaire. Une intervention est exceptionnelle</li>
                            </ul>
                          </li>
                          <li>Autres complications :
                            <ul class="level2_list">
                              <li>l'alvéolite sèche survient quelques jours après l’intervention. C’est un retard de cicatrisation, fortement favorisé par le
                                tabagisme. Elle nécessite souvent un traitement local</li>
                              <li>des gerçures des lèvres et de la face interne des joues sont des complications bénignes. Elles régressent en quelques jours</li>
                              <li>des hémorragies</li>
                            </ul>
                          </li>
                          <li>Complications dentaires :
                            <ul class="level2_list">
                              <li>la perte d’un plombage ou le descellement d’une couronne sont possibles</li>
                              <li>la persistance d’un fragment de racine. Il est parfois préférable de laisser en place un bout de racine difficile à extraire,
                                trop proche du nerf alvéolaire inférieur. Il n’y a aucune suite dans la plupart des cas</li>
                            </ul>
                          </li>
                          <li>Complications sinusiennes :<br />
                            Elles concernent exclusivement les dents de sagesse du haut qui sont proches des sinus maxillaires.
                            <ul class="level2_list">
                              <li>la communication bucco-sinusienne. Elle se ferme spontanément dans la plupart des cas. Mais sa persistance peut nécessiter un
                                traitement chirurgical</li>
                              <li>une migration anormale de la dent dans le sinus maxillaire peut survenir. Cette complication est rare. La dent doit être
                                récupérée afin d'éviter l'apparition d'une sinusite</li>
                            </ul>
                          </li>
                          <li>Complication osseuse :
                            <ul class="level2_list">
                              <li>la fracture de la mâchoire inférieure reste exceptionnelle</li>
                            </ul>
                          </li>
												</ul>
