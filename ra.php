                    <section>
                      <header>
                        <h2>La résection apicale (kyste apico-dentaire)</h2>
                      </header>
                      <p>
                        Les kystes apico-dentaires se développent à la pointe de la racine des dents mortifiées. Il s’agit d’une lésion infectieuse chronique,
                        évoluant lentement et à bas bruit.
                      </p>
                      <h3>Consultation</h3>
                      <p>
                        La consultation peut survenir dans les cas suivant :
                        <ul class="basic-list">
                          <li>Le kyste est responsable d’un abcès ou de manifestations infectieuses à répétition.</li>
                          <li>Le patient présente une pathologie ou un état de santé (diabète, prothèse valvulaire cardiaque, prothèse de hanche...) nécessitant
                            l’éradication de tout foyer infectieux risquant d’aggraver son état.</li>
                          <li>Risque de complications comme les sinusites.</li>
                        </ul>
                        Pour résoudre le problème des kystes apicaux, deux solutions thérapeutiques sont envisageables :
                        <ul class="basic-list">
                          <li>L'extraction de la dent et de son kyste (solution radicale).</li>
                          <li>La résection apicale permet de conserver la dent (risque de récidive).</li>
                        </ul>
                        Le choix entre les deux possibilités thérapeutiques dépend du volume du kyste et de sa situation anatomique (en particulier la relation
                        avec le sinus maxillaire), de l’état général du patient. Le chirurgien maxillo-facial vous expliquera la solution la plus adaptée à votre
                        cas.
                      </p>
                      <h3>L’intervention</h3>
                      <p>
                        La résection apicale consiste à supprimer le kyste dentaire et seulement l’extrémité de la racine dentaire. Elle se déroule sous
                        anesthésie locale la plupart du temps, mais peut parfois bénéficier d’une anesthésie générale. La gencive est suturée avec du fil
                        résorbable (disparaît spontanément en 3 semaines).<br />
                        Cette technique nécessite absolument que la dent à traiter soit dévitalisée. Si ce n'est pas le cas, il faudra coordonner les soins avec
                        votre dentiste afin qu'il procède au traitement canalaire de la dent. Idéalement la dévitalisation de la dent sera réalisée avant la
                        résection.
                      </p>
                      <h3>Suites opératoires</h3>
                      <p>
                        Les manifestations les plus fréquentes en post-opératoire sont :
                        <ul class="basic-list">
                          <li>des petits saignements pendant les 24 premières heures.</li>
                          <li>des douleurs contrôlées par les antalgiques prescrits et disparaîssant en général en quelques jours.</li>
                          <li>un oedème des joues : imprévisible et variable d’une personne à l’autre.</li>
                        </ul>
                        Votre ordonnance comprendra :
                        <ul class="basic-list">
                          <li>des bains de bouche, à débuter seulement 24 heures après l’intervention.</li>
                          <li>des médicaments contre la douleur (antalgiques) et/ou des anti-inflammatoires.</li>
                          <li>une vessie de glace sur la joue pendant 24 heures (effets antalgique, anti-inflammatoire et anti-oedémateux).</li>
                          <li>des antibiotiques.</li>
                          <li>une alimentation molle, tiède ou froide pendant les 24 premières heures. Ceci diminue le risque de saignements.</li>
                          <li>un arrêt du tabac pendant la période postopératoire. Le tabac favorise les complications infectieuses et de mauvaise cicatrisation.</li>
                        </ul>
                      </p>
                      <h3>Complications</h3>
                      <p>
                        Bien que tous les efforts soient mis en oeuvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser l’extraction des dents de sagesse, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>Complications hémorragiques :<br />
                            des saignements importants peuvent survenir. Ils cèdent en mordant des compresses, et dans de rares cas, un petit geste sous anesthésie locale et nécessaire.
                          </li>
                          <li>Complication infectieuse (cellulite) :<br />
                            Elle peut survenir quelques jours à quelques semaines après la résection apicale. Un traitement antibiotique associé ou non à un geste sous anesthésie locale
                            est mis en place. Une réintervention sous anesthésie générale est rare. L’infection de l’os (ostéite) est très exceptionnelle.
                          </li>
                          <li>Récidive du kyste :<br />
                            L'infection dentaire persiste malgré l'ablation du kyste et justifie alors l'extraction de la dent.
                          </li>
                          <li>Complications sinusiennes :<br />
                            Elles concernent exclusivement les résections apicales sur les dents du haut qui sont en relation étroite avec les sinus maxillaires.
                            La résection apicale peut provoquer une sinusite maxillaire.
                          </li>
                          <li>Complications nerveuses :<br />
                            Le nerf alvéolaire inférieur (droit et gauche) est responsable de la sensibilité labio-mentonnière du même côté. Sa proximité avec
                            les racines de la dent peut être source de souffrance se manifestant par une perte de la sensibilité  totale (anesthésie) ou
                            partielle (hypoesthésie) labio-mentonnière. Ces troubles de la sensibilité sont temporaires et récupèrent en quelques semaines dans
                            la grande majorité des cas.
                          </li>
												</ul>
                      </p>
