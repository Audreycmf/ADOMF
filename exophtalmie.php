											<p>
                        La décompression orbitaire est une chirurgie qui consiste à faire "de la place" dans l’orbite en enlevant de l’os,
                        plus ou moins de la graisse, afin de réduire l’<strong>exophtalmie</strong> et de faire rentrer les yeux dans les orbites. C’est le premier
                        temps chirurgical dans le traitement de la maladie de Basedow.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Consultation</h2>
                      <p>
                        La consultation va permettre de déterminer le stade clinique et le grade tomodensitométrique de l’ophtalmopathie basedowienne.
                        Le tabac est un facteur de mauvaise réponse thérapeutique.<br />
                        Un scanner et/ou une IRM vous seront prescrits afin de distinguer l’hypertrophie graisseuse de l’hypertrophie musculaire ou
                        d’une forme mixte. Cela permet de décider d’une expansion orbitaire pure ou de l’association à une lipectomie. L’imagerie
                        permet également d’éliminer une sinusite maxillaire, ethmoïdale ou frontale qui est une contre-indication temporaire à
                        l’intervention. Nous évaluons également la stabilité de l’orbitopathie basedowienne puisque l’intervention n’est réalisée
                        qu’après une période de stabilisation de 6 mois.<br />
                        En  complément, un examen ophtalmologie préopératoire complet vous sera demandé.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">L’intervention</h2>
                      <p>
                        Elle se déroule sous anesthésie générale au cours d’une hospitalisation de 2 jours. Aucune incision n’est visible puisque l’on
                        passe par l’intérieur de la paupière de chaque œil.<br />
                        En fonction de l’examen du scanner ou de l’IRM, on retire l’os au niveau du plancher de l’orbite en conservant une bande
                        au-dessus du nerf infra-orbitaire (qui fait la sensibilité de votre  joue, mâchoire supérieure et partie latérale  du nez) pour
                        le protéger. On retire également de l’os de la paroi interne. Une lipectomie (exérèse de graisse) orbitaire sera éventuellement
                        réalisée en complément.<br />
                        L’intervention dure environ 1h30.
                      </p>
                      <h2><img src="assets/css/images/icon-checkmark.png" alt="checkmark rouge" class="coche_rouge">Suites opératoires</h2>
                      <p>
                        Les suites opératoires sont peu douloureuses.<br />
                        Des compresses glacées seront placées sur vos yeux afin de diminuer l’œdème et les ecchymoses post-opératoires. Elles ont
                        également un effet antalgique.<br />
                        Vous bénéficierez d’un traitement intraveineux par antibiotiques, corticoïdes et antalgiques. Il sera nécessaire de mettre
                        régulièrement de la pommade dans vos yeux afin d’éviter une irritation de vos cornées.<br />
                        Il vous sera interdit de vous moucher pendant 6 semaines.
                      </p>
