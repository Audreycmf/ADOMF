											<header>
												<h2>La parotidectomie</h2>
											</header>
											<p>
                        La parotidectomie est l'ablation partielle ou totale de la glande parotide. La <a href="#parotide">glande parotide</a> est une des glandes salivaires
                        principales. Nous en avons deux, une à droite et une à gauche en avant de chaque oreille (ceux sont elles qui gonflent et
                        deviennent visibles lors des oreillons). Elles sont traversées par le nerf facial. Ce nerf permet la motricité de la
                        moitié de la face (un pour chaque côté du visage).
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Consultation</h3>
                      <p>
                        L’apparition d’une masse, le plus souvent indolore, de la région parotidienne amène à consulter. L’examen clinique permet
                        d’orienter le diagnostic. Des examens (scanner, IRM) permettent d’affiner le diagnostic. Dans la grande majorité des cas, il
                        s’agit d’une tumeur dont il est indispensable de préciser la nature bénigne ou maligne (dans environ 70% des cas, il s’agit de
                        tumeur bénigne). La parotidectomie, associée à un examen histologique (microscopique), est à la fois le moyen d’établir le
                        diagnostic de façon certaine et dans le même temps de réaliser le traitement. Beaucoup plus rarement, la pathologie est
                        d’origine lithiasique (calcul) ou inflammatoire.
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">L’intervention</h3>
                      <p>
                        L'intervention se déroule sous anesthésie générale et donc lors d’une hospitalisation.<br />
                        <a href="#incision">L'incision</a> passe juste en avant de l'oreille (type incision du lifting cervico-facial), puis se poursuit sous l’oreille au
                        niveau du cou, le but étant de rendre discrète la cicatrice. Le nerf facial est repéré et écarté pour effectuer l'ablation de
                        la tumeur. La tumeur est analysée pendant l'intervention (examen anatomopathologique extemporané) afin de préciser la nature de
                        celle-ci. Le  résultat guide  la suite de l’intervention :
                        <ul class="basic-list">
                          <li>tumeur bénigne : le geste est déjà complet, la parotidectomie reste partielle</li>
                          <li>tumeur mixte (ou adénome pléomorphe) : cette tumeur bénigne est la plus fréquente de la glande parotide ; elle présente
                            un risque de récidive et de cancérisation rare mais possible. La parotidectomie est complétée pour être quasi totale</li>
                          <li>tumeur maligne : la parotidectomie sera totale, complétée par un curage ganglionnaire cervical (traitement des ganglions
                            du cou) grâce à un prolongement de l'incision dans le cou</li>
                        </ul>
                        En fin d’intervention, un drain est mis en place pour évacuer l’hématome post-opératoire.
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Suites opératoires</h3>
                      <p>
                        La durée de l’hospitalisation varie de 2 à 5 jours (ablation du drainage).<br />
                        Les soins opératoires comportent des soins locaux pendant une période de 8 à 10 jours jusqu’à l’ablation des points.
                        Les suites post-opératoires sont :
                        <ul class="basic-list">
                          <li>une dépression résiduelle existant parfois en arrière de l'angle de la mâchoire</li>
                          <li>une perte de sensibilité du pavillon de l'oreille fréquente en post-opératoire. Cela s'atténuera avec le temps</li>
                          <li>une parésie (paralysie incomplète) de l’hémiface du côté opéré. Son intensité est variable. Elle est temporaire. La
                            récupération d'une mobilité normale prend environ 1 à 3 mois. Lorsque la parésie est importante, des séances de
                            kinésithérapie seront  prescrites associées à une protection oculaire nocturne (le déficit de fermeture de l’oeil expose la
                            cornée à des ulcérations)</li>
                        </ul>
                      </p>
                      <h3><img src="assets/css/images/icon-checkmark.png" alt="" class="coche_rouge">Complications</h3>
                      <p>
                        Bien que tous les efforts soient mis en oeuvre dans des conditions de compétence et de sécurité conformes aux données actuelles de la
                        science pour réaliser une parotidectomie, le risque de complication n’est pas nul.<br />
                        En  choisissant un chirurgien qualifié, formé spécifiquement à ce type de techniques, vous limitez au maximum ces risques, sans toutefois
                        les supprimer complètement. Les complications significatives restent exceptionnelles. Il faut mettre en balance les risques encourus par
                        rapport aux bénéfices de l’intervention.<br />
                        Pour autant, et malgré leur rareté, vous devez quand même connaître les complications possibles :
                        <ul class="basic-list">
                          <li>les hémorragies et surinfections sont exceptionnelles</li>
                          <li>le syndrome de Frey : ce phénomène apparaît après quelques mois l’intervention. Il s’agit d’un défaut de cicatrisation de
                            certaines terminaisons nerveuses. Lors de l'alimentation, une hypersudation apparaît sur la joue, d'intensité variable,
                            parfois gênante. Ce syndrome peut être désormais résolu par des injections de toxine botulique</li>
                          <li>la paralysie faciale définitive est exceptionnelle. Elle peut être volontaire par sacrifice délibéré du nerf (tumeur
                            maligne) ou survenir lors d’une chirurgie techniquement complexe (tumeur inflammatoire ou infectée, seconde intervention)</li>
												</ul>
											</p>
